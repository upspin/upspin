// +build ignore

// The make_version program is run by go generate to compile a version stamp
// to be compiled into the upspin binary.
// It does nothing unless $COMMIT_SHA is set, which is true only during
// the release process.
package main

import (
	"fmt"
	"io/ioutil"
	"log"
	"os"
)

func main() {
	log.SetPrefix("make_version")
	log.SetFlags(0)

	version := os.Getenv("COMMIT_SHA")
	if version == "" {
		return
	}

	output := fmt.Sprintf(outputFormat, version)

	err := ioutil.WriteFile("git_version.go", []byte(output), 0664)
	if err != nil {
		log.Fatal(err)
	}
}

const outputFormat = `
// Code generated by 'go run makeversion.go'. DO NOT EDIT.

package main

import (
	"fmt"
	"time"
)

func init() {
	buildTime = fmt.Sprintf("%%s", time.Now().In(time.UTC).Format(time.Stamp+" UTC"))
	gitSHA = fmt.Sprintf("%%s", %q)
}
`
